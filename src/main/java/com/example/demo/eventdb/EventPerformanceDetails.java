package com.example.demo.eventdb;

public class EventPerformanceDetails {

      private String eventId;
      private double numberOfStudent;
      private double numberOfStudentRatedGive;

    public String getEventId() {
        return eventId;
    }

    public void setEventId(String eventId) {
        this.eventId = eventId;
    }

    public double getNumberOfStudent() {
        return numberOfStudent;
    }

    public void setNumberOfStudent(double numberOfStudent) {
        this.numberOfStudent = numberOfStudent;
    }

    public double getNumberOfStudentRatedGive() {
        return numberOfStudentRatedGive;
    }

    public void setNumberOfStudentRatedGive(double numberOfStudentRatedGive) {
        this.numberOfStudentRatedGive = numberOfStudentRatedGive;
    }
}
